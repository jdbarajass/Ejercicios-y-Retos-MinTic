    private void txtEdadKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEdadKeyReleased
        // KeyReleased dispara un evento cuando presiono una tecla 
        JOptionPane.showMessageDialog(rootPane, txtEdad.getText(), "Importante", JOptionPane.INFORMATION_MESSAGE);
        try {
            int edad = Integer.parseInt(txtEdad.getText());
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(rootPane,"Esto no es un número "," Importante ", JOptionPane.INFORMATION_MESSAGE); // Me genera un Msn si lo que ingrese no es un número
            txtEdad.setText(txtEdad.getText().substring(0, txtEdad.getText().length() - 1)); // Cada vez que ingresa una letra la borra solo deja ingresa numeros, me recorta el ultimo indice
        }
    }//GEN-LAST:event_txtEdadKeyReleased
